    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton1ActionPerformed
    {//GEN-HEADEREND:event_jButton1ActionPerformed
        if (modelo.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Falt칩 por llenar el campo Modelo", "Alerta", JOptionPane.WARNING_MESSAGE);
            modelo.requestFocus();
        } else if (fabricante.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Falt칩 por llenar el campo Fabricante", "Alerta", JOptionPane.WARNING_MESSAGE);
            fabricante.requestFocus();
        } else if (Integer.parseInt(anio.getText()) < 1886 || Integer.parseInt(anio.getText()) > 2025)
        {
            JOptionPane.showMessageDialog(this, "A침o invalido", "Alerta", JOptionPane.WARNING_MESSAGE);
            anio.requestFocus();
        } else if (Integer.parseInt(precio.getText()) < 0 || Integer.parseInt(precio.getText()) > 999999999)
        {
            JOptionPane.showMessageDialog(this, "A침o invalido", "Alerta", JOptionPane.WARNING_MESSAGE);
            precio.requestFocus();
        } else
        {
            Autos a = new Autos();
            a.altaAuto(modelo, transmision, anio, tipo, precio, fabricante);
            JOptionPane.showMessageDialog(this, "Auto agregado con 칠xito");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

